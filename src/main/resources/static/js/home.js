 function getMap(long, late, zoom) {
     require([
         "esri/Map",
         "esri/views/MapView",
         "esri/Graphic",
         "esri/layers/GraphicsLayer",
         "esri/widgets/Zoom"
     ], function(Map, MapView, Graphic, GraphicsLayer, Zoom) {

         var map = new Map({
             basemap: "topo-vector"
         });
         map.on("load", function() {
             map.graphics.enableMouseEvents();
         });


         var view = new MapView({
             container: "viewDiv",
             map: map,
             center: [long, late],
             zoom: zoom,
             highlightOptions: {
                 color: "blue"
             }
         });
         //  view.popup.autoOpenEnabled = false;
         var createGraphic = function(data) {
             return new Graphic({
                 geometry: data,
                 symbol: data.symbol,
                 attributes: data,
                 popupTemplate: data.popupTemplate
             });
         };
         var createTextGraphic = function(data) {
             return new Graphic({
                 geometry: {
                     type: data.type,
                     longitude: data.longitude,
                     latitude: data.latitude,
                 },
                 symbol: data.symbol2,
             });
         }
         var createPictureGraphic = function(data) {
             return new Graphic({
                 geometry: {
                     type: data.type,
                     longitude: data.longitude,
                     latitude: data.latitude,
                 },
                 symbol: data.symbol1,
             });
         }



         var graphicsLayer = new GraphicsLayer();

         jsondata.polygons.forEach(function(data) {
             graphicsLayer.add(createGraphic(data));
         });
         jsondata.points.forEach(function(data) {
             graphicsLayer.add(createGraphic(data));
         });
         jsondata.points.forEach(function(data) {
             graphicsLayer.add(createTextGraphic(data));
         });
         jsondata.points.forEach(function(data) {
             graphicsLayer.add(createPictureGraphic(data));
         });
         map.add(graphicsLayer);
         var graphicsLayer1 = new GraphicsLayer();
         var data1 = {
             type: "point",
             longitude: long,
             latitude: late,
             Name: "Landmark 81",
             Location: "Khu Tân Cảng, 720A Điện Biên Phủ, Phường 22, Bình Thạnh, Thành phố Hồ Chí Minh, Việt Nam",
             symbol: {
                 type: "simple-marker",
                 color: [0, 0, 0, 0],
                 size: "30px",
                 outline: {
                     color: [255, 255, 255],
                     width: 0
                 }
             },
             symbol1: {
                 type: "picture-marker",
                 url: "./img/marker1.png",
                 width: "50px",
                 height: "50px"
             },
         }
         graphicsLayer1.add(createGraphic(data1));
         graphicsLayer1.add(createPictureGraphic(data1))
         map.add(graphicsLayer1);

     });
 }
